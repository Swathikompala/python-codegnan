x = 10
y = 5

print("x =", x)
print("y =", y)

# Logical AND
print("x > 5 and y < 10:", x > 5 and y < 10)  # True and True => True
print("x > 5 and y > 10:", x > 5 and y > 10)  # True and False => False

# Logical OR
print("x > 5 or y > 10:", x > 5 or y > 10)    # True or False => True
print("x < 5 or y > 10:", x < 5 or y > 10)    # False or False => False

# Logical NOT
print("not(x > 5):", not(x > 5))              # not(True) => False
print("not(y > 10):", not(y > 10))            # not(False) => True
